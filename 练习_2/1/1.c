#include <reg52.h>
#define uint unsigned int 
/**************************************************************
***         8个发光管来回流动，没个管亮100ms, 					  	***
***        流动时让蜂鸣器发出“滴滴”声     				        	***
**************************************************************/

/***第一个发光管以间隔200ms闪烁***/

sbit D1 = P2^0;

void delay(uint a)
{
	while(a--);
}

void main()
{
	D1=0;
	while(1)
	{
		D1=~D1;
		delay(44500);
	}
}
	